public class IIB {

	private String bipservice;
	private String bipbroker;
	private String biphttplistener;
	private String bipMasterService;
	private String dataFlowEngine;

	public String getBipservice() {
		return bipservice;
	}

	public void setBipservice(String bipservice) {
		this.bipservice = bipservice;
	}

	public String getBipbroker() {
		return bipbroker;
	}

	public void setBipbroker(String bipbroker) {
		this.bipbroker = bipbroker;
	}

	public String getBiphttplistener() {
		return biphttplistener;
	}

	public void setBiphttplistener(String biphttplistener) {
		this.biphttplistener = biphttplistener;
	}

	public String getBipMasterService() {
		return bipMasterService;
	}

	public void setBipMasterService(String bipMasterService) {
		this.bipMasterService = bipMasterService;
	}

	public String getDataFlowEngine() {
		return dataFlowEngine;
	}

	public void setDataFlowEngine(String dataFlowEngine) {
		this.dataFlowEngine = dataFlowEngine;
	}

}
